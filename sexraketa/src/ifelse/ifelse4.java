package ifelse;

public class ifelse4 {

	public static void main(String[] args) {
		int a = 19;
		int b = -22;
		int c = 8;
		int countPositive = 0;
		if (a > 0) countPositive++;
		if (b > 0) countPositive++;
		if (c > 0) countPositive++;
		System.out.println(countPositive);

	}

}
